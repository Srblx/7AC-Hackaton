<h1>Les Bermudes - Lorient.</h1>

<div class="containt_event">
    <p>
    Ce qui attend les participants : Un trajet d'environ 3 000 miles difficile à négocier et riche en phénomènes majestueux : grands courants, dépressions extra-tropicales,
richesse de la faune marine, et parfois même, icebergs !
    </p>
    <p>
    Les objectifs ? Tenter d’établir un nouveau record de traversée - 12 jours 23 h et 16 min
    </p>
    <p>
    Mais SURTOUT, lever des fonds au profit de la recherche océanique ! Chaque équipage se lance dans ce défi afin de sensibiliser sur les enjeux liés à la protection de
    l'océan et donner les moyens au plus grand nombre de contribuer à sa sauvegarde.
    </p>
    <p>
    À cet effet, une cagnotte vient d'être mise en ligne : https://lnkd.in/dfdjicgd
    </p>
    <h4>Plus d’informations sur le parcours :</h4>
    <p>
    Saint George’s – Lorient : environ 2880 milles nautiques Les participants n’auront pas à respecter de portes de glaces et seront libres, sous leur propre responsabilité,
avec leurs routeurs ou non de choisir leur parcours.
    </p>
    <p>
    DÉPART La ligne de départ est matérialisée par :
    <ul>
        <li>
        SB “Safe Water Buoy” par 32° 22 .944' N & 064°37.071’ W <br>
(éclat rouge et blanc toutes les 6 secondes) à laisser à bâbord
        </li>
        <li>
        “St David Lighthouse” par 32°21.8’ ́N & 064°39’ 1’ ́W, à laisser à tribord.
        </li>
    </ul>
    </p>
