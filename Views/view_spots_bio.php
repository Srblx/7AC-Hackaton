    <h2>L'impact climatique sur la biodiversité</h2>
    <p>
    Plus de 90 % de la chaleur générée dans l’atmosphère par nos émissions de gaz à effet de serre
    est absorbée par l’océan. Le réchauffement du climat est donc avant tout un réchauffement de
    l’océan !
    </p>
    <p>
    Un phénomène alarmant, avec des lourdes conséquences pour les écosystèmes marins et les
    sociétés humaines !
    </p>
    <p>
    La dilatation thermique : l'eau se dilate en se réchauffant et prend donc plus de place. Combiné à la
    fonte des glaciers et des calottes polaires, ce phénomène engendre une hausse du niveau des mers,
    estimée à 50-90cm d’ici à 2100. 
    </p>
    <p>
    L’aggravation de l’intensité et de la fréquence de certains évènements météorologiques extrêmes :
    un océan plus chaud fournit plus de « carburant » aux cyclones et autres tempêtes tropicales ; les
    épisodes de pluies extrêmes sont intensifiés etc.   
    </p>
    <p>
    La désoxygénation : l’eau de mer plus chaude dissout moins l’oxygène. Hypoxiques, certaines de
    ces « dead zones » deviennent irrespirables pour certains organismes (poissons, coraux, bivalves) et
    en favorisent d’autres (méduses, algues, etc.).
    </p>
